Levels.prototype.init=function(){

  this.Hud=new Image();
  this.Hud.src='assets/Level_Assets/Hud/Hud.png';

  this.level1Grass=new Image();
  this.level1Grass.src='assets/Level_Assets/Level1_Assets/Grass.png';

  this.level1StoneWall=new Image();
  this.level1StoneWall.src='assets/Level_Assets/Level1_Assets/StoneWall.png';

  this.level2River= new Image();
  this.level2River.src = 'assets/Level_Assets/Level2_Assets/River.png'

  this.level2MudGround= new Image();
  this.level2MudGround.src = 'assets/Level_Assets/Level2_Assets/MudGround.png'


}

Levels.prototype.draw=function(ctx){
  if(TutorialLevel === true || FirstLevel === true)
  {
      for (i=0;i<28;i++)
      {
        for(j=0;j<15;j++)
        {
          if(level.Level1[i][j]==="Hud")
          {
            app.ctx.drawImage(this.Hud,j*64,i*64,64,64);
          }
          if(level.Level1[i][j]==="StoneWall")
          {
            app.ctx.drawImage(this.level1StoneWall,j*64,i*64,64,64);
          }
          if(level.Level1[i][j]==="Grass")
          {
            app.ctx.drawImage(this.level1Grass,j*64,i*64,64,64);
          }
        }
      }
    }

    if(SecondLevel === true)
    {
      for (i=0;i<28;i++)
      {
        for(j=0;j<15;j++)
        {
          if(level.Level2[i][j]==="River")
          {
            app.ctx.drawImage(this.level2River,j*64,i*64,64,64);
          }
          if(level.Level2[i][j]==="MudGround")
          {
            app.ctx.drawImage(this.level2MudGround,j*64,i*64,64,64);
          }
        }
      }
    }
}
